loan_tenure.addEventListener("input",function(){
    var loan_amount = document.getElementById("loan_amount");
    var interest_rate = (document.getElementById("interest_rate").value)*0.01;
    var loan_tenure = document.getElementById("loan_tenure");
    var formula = 1 + interest_rate;
    var ta=1;
    if(loan_tenure.value == ""){
        total_payment.innerText= ta*loan_amount.value;
    }else{
    for(let i=0;i<loan_tenure.value;i++) {
        ta = ta * formula;
        total_payment.innerText= (ta*loan_amount.value).toFixed(2);
        total_interest.innerText=(ta*loan_amount.value-loan_amount.value).toFixed(2);
        loan_emi.innerText = (ta*loan_amount.value/loan_tenure.value).toFixed(2);
    }
}
});
